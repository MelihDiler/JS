//                         ---------- Karşılaştırma Operatörleri ----------

//?                                   == Operatörü
console.log(5 == 5);               //?     Çıktı true olur.

//*                                   != Operatörü
console.log(5 != 3);               //*     Çıktı true olur. Çünkü eşit değil mi diye sorduk ve evet eşit değildir.

//TODO                                > Operatörü
console.log(5 > 3);                //TODO  Çıktı true olur. 5, 3'den büyüktür.

//!                                   < Operatörü
console.log(5 < 3);                //!     Çıktı false olur. 5, 3^den küçük değildir.

//?                                   >= Operatörü
console.log(12 <= 12);             //?     Çıktı true olur. 12, 12'den küçük değil ama eşit.

//*                                   <= Operatörü
console.log(15 >= 15);             //*     Çıktı true olur. 15, 15'den büyük değil ama eşittir.
    
