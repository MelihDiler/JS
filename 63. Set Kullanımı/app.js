//                             ---------- SET KULLANIMI ----------

//     Map'ten farkı benzersiz değerlerden oluşan bir koleksiyondur. Aynı değeri birden fazla kez içeremez ve anahtar yoktur sadece değer.
//     Elemanlar eklenme sırasına göre tutulur, ancak sıralı bir yapı değildir.
//     Elemanlara doğrudan erişim yoktur, döngü veya has() metodu ile kontrol edilir.

const set = new Set();

//?--------------------------------------------------------------------------------------------------------------------------------------
//?                            ---------- DEGER EKLEME ----------
//?--------------------------------------------------------------------------------------------------------------------------------------

set.add(true);
set.add(3.14);
set.add("Melih");
set.add("Diler");
set.add("Diler");
set.add("Diler");
set.add("Diler");
set.add(7);
set.add({ userName: "Melih", password: "1" });
set.add([1, 2, 3, 4]);


//?--------------------------------------------------------------------------------------------------------------------------------------
//*--------------------------------------------------------------------------------------------------------------------------------------
//*                            ---------- MIKTAR SORMA ----------
//*--------------------------------------------------------------------------------------------------------------------------------------

console.log(set.size);                                                             //*     Çıktı = 7     Çünkü aynı değer olan "Diler" i 1 deb fazla ekleyemezsin.

//*--------------------------------------------------------------------------------------------------------------------------------------
//TODO-----------------------------------------------------------------------------------------------------------------------------------
//TODO                         ---------- DEGER SILME ----------
//TODO-----------------------------------------------------------------------------------------------------------------------------------

set.delete("Diler");
set.delete([1, 2, 3, 4]);                                                          //*     Bunları silemezsin. Önce değişkene atayıp o değişkeni ekleyip silmelisin. Referans değerleri farklıdır.
console.log(set.size);                                                             //*     Çıktı = 6

//TODO-----------------------------------------------------------------------------------------------------------------------------------
//!--------------------------------------------------------------------------------------------------------------------------------------
//!                            ---------- DEGER SORGULAMA ----------
//!--------------------------------------------------------------------------------------------------------------------------------------

console.log(set.has("Melih"));                                                     //!     Çıktı = true
console.log(set.has([1, 2, 3, 4]));                                                //!     Çıktı = false      Çünkü referans tiplidir. Önce değişkene atayıp sonra set koleksiyonu içine tanımlarsan bulabilirsin.
//!--------------------------------------------------------------------------------------------------------------------------------------
//?--------------------------------------------------------------------------------------------------------------------------------------
//?                            ---------- FOR OF İLE DONME ----------
//?--------------------------------------------------------------------------------------------------------------------------------------

for (let value of set) {
    console.log(value);                                                            //?     Çıktı = hepsini alt alta doğru verdi.
}

//?--------------------------------------------------------------------------------------------------------------------------------------
//*--------------------------------------------------------------------------------------------------------------------------------------
//*                    ---------- ARRAY'E CEVIREREK DEGERLERI ALMA ----------
//*--------------------------------------------------------------------------------------------------------------------------------------

const values = Array.from(set);
console.log(values);                                                               //?     Çıktı = [true, 3.14, 'Melih', 7, {…}, Array(4)]

