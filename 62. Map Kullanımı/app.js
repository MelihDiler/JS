const map1 = new Map();

//?--------------------------------------------------------------------------------------------------------------------------------------
//?                                  ---------- ANAHTAR - DEGER EKLEME ----------
//?--------------------------------------------------------------------------------------------------------------------------------------

map1.set(34, "İstanbul");
map1.set(35, "İzmir");
map1.set(6, "Ankara");
map1.set(1, "Adana");

//?--------------------------------------------------------------------------------------------------------------------------------------
//*--------------------------------------------------------------------------------------------------------------------------------------
//*                                        ---------- VERI CEKME ----------
//*--------------------------------------------------------------------------------------------------------------------------------------

console.log(map1.get(6));                                                          //*     Çıktı = Ankara
//*----------
//*     VEYA:
//*----------
const donenDeger = map1.get(6);                                                    //*     Buradaki önemli nokta sana bir değer döndürmesi.
console.log(donenDeger);                                                           //*     Çıktı = Ankara

//*--------------------------------------------------------------------------------------------------------------------------------------
//TODO-----------------------------------------------------------------------------------------------------------------------------------
//TODO                                    ---------- MIKTAR SORMA ----------
//TODO-----------------------------------------------------------------------------------------------------------------------------------

let value = map1.size;
console.log(value);                                                                //TODO  Çıktı = 4

//TODO-----------------------------------------------------------------------------------------------------------------------------------
//!--------------------------------------------------------------------------------------------------------------------------------------
//!                                       ---------- ANAHTAR SILME ----------
//!--------------------------------------------------------------------------------------------------------------------------------------

let bool = map1.delete(34);
console.log(bool);                                                                 //!     Çıktı = true    Sildiğine dair değer döndürür.

//!--------------------------------------------------------------------------------------------------------------------------------------
//?--------------------------------------------------------------------------------------------------------------------------------------
//?                                     ---------- ANAHTAR SORGULAMA ----------
//?--------------------------------------------------------------------------------------------------------------------------------------

console.log(map1.has(55));                                                         //?     Çıktı = false   İçinde "55" numaralı anahtar yok.

//?--------------------------------------------------------------------------------------------------------------------------------------
//*--------------------------------------------------------------------------------------------------------------------------------------
//*                                      ---------- UZERINDE DONMEK ----------
//*--------------------------------------------------------------------------------------------------------------------------------------

for (let [key, value] of map1) {                                                   //*     Burada Destructing kullandık map1 içindeki her alan key ve value olarak oluşur. Bizde onları key value adındaki değişkenlere sırayla ata dedik. for (let (value) of map1) yapsaydık ve console.log(value); deseydik key ve value değerini bir dizi olarak atayacaktı
    console.log(key, value);                                                       //*     Çıktı = 35 İzmir / 6 Ankara / 1 Adana
}

//*--------------------------------------------------------------------------------------------------------------------------------------
//TODO-----------------------------------------------------------------------------------------------------------------------------------
//TODO                              ---------- FOREACH ILE KEYLERDE DONME ----------
//TODO-----------------------------------------------------------------------------------------------------------------------------------

// const keys = map1.keys();
// console.log(keys);                                                              //TODO  Çıktı = {35, 6, 1}     Bu bir  Array değildr.
const keys = Array.from(map1.keys());                                              //TODO  Array'e çevirip keys değişkenine array olarak atadık.
console.log(keys);                                                                 //TODO  Çıktı = [35, 6, 1]     Bu bir Arraydir.

keys.forEach((key) => {
    console.log(key, map1.get(key));                                               //TODO  , map1.get(key) eklersen value değeri de gelmiş olur.
})



//TODO-----------------------------------------------------------------------------------------------------------------------------------
//!--------------------------------------------------------------------------------------------------------------------------------------
//!                              ---------- FOR OF İLE KEY VE VALUE'DA DONME ----------
